static const int* pieceTables[] = {
    pawnTable, knightTable, bishopTable, rookTable, queenTable, kingMiddleTable
};

static int mirror(int sq) {
    return makeSquare(fileOf(sq), 7 - rankOf(sq));
}

int evaluate(const Board& b) {
    int score = 0;
    for (int sq = 0; sq < 64; sq++) {
        if (b.pieces[sq] == PieceNone) continue;
        int p = b.pieces[sq];
        int val = materialVal[p];
        int tableIdx = (b.colors[sq] == White) ? mirror(sq) : sq;
        int posVal = pieceTables[p][tableIdx];

        if (b.colors[sq] == White)
            score += val + posVal;
        else
            score -= val + posVal;
    }
    return (b.side == White) ? score : -score;
}
